/*某校的惯例是在每学期的期末考试之后发放奖学金。发放的奖学金共有五种，获取的条件各自不同:
(1)院士奖学金，每人8000元，期末平均成绩高于80分(>80)，
并且在本学期内发表1篇或1篇以上论文的学生均可获得;
(2)五四奖学金，每人4000元，期末平均成绩高于85分(>85)，
并且班级评议成绩高于80分(>80)的学生均可获得;
(3)成绩优秀奖，每人2000元，期末平均成绩高于90分(>90)的学生均可获得;
(4)西部奖学金，每人1000元，期末平均成绩高于85分(>85)的西部省份学生均可获得;
(5) 班级贡献奖，每人850元，班级评议成绩高于80分(>80)的学生干部均可获得;
只要符合条件就可以得奖，每项奖学金的获奖人数没有限制，每名学生也可以同时获得多项奖学金。

例如姚林的期末平均成绩是87分，班级评议成绩82分，同时他还是一位学生干部，
那么他可以同时获得五四奖学金和班级贡献奖，奖金总数是4850元。
现在给出若干学生的相关数据，请计算哪些同学获得的奖金总数最高(假设总有同学能满足获得奖学金的条件)

输入
输入的第一行是一个整数N(1<=N<=100)，表示学生的总数。
接下来的N行每行是一位学生的数据，从左向右依次是姓名，期末平均成绩，
班级评议成绩，是否是学生干部，是否是西部省份学生，以及发表的论文数。
姓名是由大小写英文字母组成的长度不超过20的字符串(不含空格);
期末平均成绩和班级评议成绩都是0到100之间的整数(包括0和100);
是否是学生干部和是否是西部省份学生分别用一个字符表示，Y表示是，N表示不是;
发表的论文数是0到1o的整数(包括0和10)。每两个相邻数据项之间用一个空格分隔

输出
输出包括三行，
第一行是获得最多奖金的学生的姓名，
第二行是这名学生获得的奖金总数。
如果有两位或两位以上的学生获得的奖金最多，
输出他们之中在输入文件中出现最早的学生的姓名。
第三行是这N个学生获得的奖学金的总数

样例输入
4
YaoLin 87 82 Y N 0
ChenRuiyi 88 78 N Y 1
Lixin 92 88 N N 0
ZhangQin 83 87 Y N 1

样例输出
ChenRuiyi
9000
28700
*/



#include<stdio.h>
#include<string.h>   // 新增：为了 strcpy

int main (){
    int n;
    scanf("%d",&n);
    int sum[100] = {0};      // 改成固定大小100，并初始化所有为0
    int N = n;               // 保存原始人数（因为 while 会改变 n）
    char max_name[21];       // 新增：存最高奖金学生姓名
    int max_money = -1;      // 新增：存最高奖金金额
    int Sum = 0;             // 你的 Sum 变量，初始化为0

    while(n--){
        char name[21];
        int avg_num, class_num, lun;
        char stu, xi;
        // 修正 scanf：不要 &name，%c 前加空格，去掉末尾空格
        scanf("%s %d %d %c %c %d", name, &avg_num, &class_num, &stu, &xi, &lun);
        
        int cnt = 0;         // 复用你的 cnt 变量，用来存当前学生奖金
        
        if(avg_num > 80 && lun >= 1){
            cnt += 8000;
        }	
        if(avg_num > 85 && class_num > 80){
            cnt += 4000;
        }
        if(avg_num > 90){
            cnt += 2000;
        }
        if(avg_num > 85 && xi == 'Y'){	
            cnt += 1000;
        }
        // 修正条件：应该是 class_num > 80，不是 avg_num > 80
        if(class_num > 80 && stu == 'Y'){
            cnt += 850;
        }
        
        // 将当前学生奖金存入 sum 数组（下标从 N-1 到 0，但没关系）
        sum[n] = cnt;         // 注意 n 是递减的，这样存是反序，但不影响
        
        // 累加总额（用你的 Sum）
        Sum += cnt;
        
        // 更新最高记录
        if(cnt > max_money){
            max_money = cnt;
            strcpy(max_name, name);
        }
    }
    
    // 输出
    printf("%s\n", max_name);
    printf("%d\n", max_money);
    printf("%d\n", Sum);
    
    return 0;
}
